<script>
    import marked from 'marked';

    export let content = '';
    export let source = '';
    export let isOpen = false;

    export async function open(newSource) {
        source = newSource;
        if (source) {
            fetch(source).then(response => response.text()).then(text => {
                content = text
                isOpen = true;
            });
        } else {
            isOpen = true;
        }
    }
    export function close() {
        isOpen = false;
    }
</script>

<div class="modal" class:hidden="{!isOpen}">
	<a class="close" on:click={close}>Ã—</a>

    <div class="modal-body">
        {@html marked(content)}
    </div>
    <div class="modal-footer">
	    <button on:click={close} class="button">Close</button>
    </div>
</div>


